var distance_between = function(a, b){
  var a = new Array(a['lat'],a['lng'])

  var rad_per_deg = Math.PI / 180
  var rkm = 6371
  var rm = rkm * 1000

  var dlon_rad = (b[1]-a[1]) * rad_per_deg
  var dlat_rad = (b[0]-a[0]) * rad_per_deg

  var lat1_rad = a[0]*rad_per_deg
  var lon1_rad = a[1]*rad_per_deg
  var lat2_rad = b[0]*rad_per_deg
  var lon2_rad = b[1]*rad_per_deg

  a = (Math.sin(dlat_rad/2)*Math.sin(dlat_rad/2)) +
      Math.cos(lat1_rad) * Math.cos(lat2_rad) *
      (Math.sin(dlon_rad/2) * Math.sin(dlon_rad/2))

  return rm * 2 * Math.asin(Math.sqrt(a))
};

Array.prototype.near = function(b,limit){
  var limit = typeof(limit) == 'undefined' ? 10 : parseInt(limit,10)

  for(var a in this){
    this[a].distance_from_origin = distance_between(this[a],b)
  };

  return this.sort(function(a,b){
    return a.distance_from_origin-b.distance_from_origin
  }).slice(0,limit)
};

